#!/usr/bin/env node
/**
 * Batch-generate JSON/CSS/Tailwind/TS variants for every brand in design-md/.
 * Writes the companion files directly next to each brand's DESIGN.md so the
 * repo showcases the multi-format capability without requiring CLI install.
 *
 * Usage:
 *   node packages/cli/scripts/generate-all-variants.js                  # write all brands
 *   node packages/cli/scripts/generate-all-variants.js --check          # verify all brands
 *   node packages/cli/scripts/generate-all-variants.js stripe linear    # selected brands
 */

import { readdir, readFile, writeFile, stat } from 'node:fs/promises';
import { resolve, join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

import { parseDesignMd } from '../src/utils/parser.js';
import { toJson } from '../src/generators/json.js';
import { toCss } from '../src/generators/css.js';
import { toTailwind } from '../src/generators/tailwind.js';
import { toTypeScript } from '../src/generators/typescript.js';
import { DIALS } from '../src/data/dials.js';

const here = dirname(fileURLToPath(import.meta.url));
const root = resolve(here, '..', '..', '..', 'design-md');
const playgroundBundlePath = resolve(here, '..', '..', '..', 'playground', 'catalog-data.js');

async function main() {
  const rawArgs = process.argv.slice(2);
  const checkMode = rawArgs.includes('--check');
  const quietMode = rawArgs.includes('--quiet');
  const args = rawArgs.filter((arg) => !arg.startsWith('--'));
  const entries = await readdir(root, { withFileTypes: true });
  const allBrands = entries.filter((e) => e.isDirectory()).map((e) => e.name);
  const targets = args.length > 0 ? args : allBrands;

  let ok = 0;
  let skipped = 0;
  const failed = [];
  const bundleData = {};

  for (const brand of targets) {
    const brandDir = resolve(root, brand);
    const mdPath = join(brandDir, 'DESIGN.md');
    try {
      await stat(mdPath);
    } catch {
      skipped++;
      continue;
    }

    try {
      const md = await readFile(mdPath, 'utf8');
      const tokens = parseDesignMd(md, { brand, dials: DIALS[brand] });
      const outputs = {
        'design.json': toJson(tokens),
        'variables.css': toCss(tokens),
        'tailwind.config.js': toTailwind(tokens),
        'tokens.ts': toTypeScript(tokens),
      };
      bundleData[brand] = {
        md,
        json: outputs['design.json'],
        css: outputs['variables.css'],
        tailwind: outputs['tailwind.config.js'],
        ts: outputs['tokens.ts'],
      };

      if (checkMode) {
        for (const [file, expected] of Object.entries(outputs)) {
          const actual = await readFile(join(brandDir, file), 'utf8');
          if (actual !== expected) {
            throw new Error(`${file} out of sync`);
          }
        }
      } else {
        for (const [file, contents] of Object.entries(outputs)) {
          await writeFile(join(brandDir, file), contents);
        }
      }

      const cCount = Object.keys(tokens.colors).length;
      const fCount = tokens.typography.families.length;
      if (!quietMode) {
        const modeLabel = checkMode ? 'in sync' : `${cCount} colors, ${fCount} fonts`;
        console.log(`  ✓ ${brand.padEnd(16)} ${modeLabel}`);
      }
      ok++;
    } catch (err) {
      failed.push({ brand, error: err.message });
      if (!quietMode) {
        console.log(`  ✗ ${brand} — ${err.message}`);
      }
    }
  }

  if (!quietMode) {
    console.log('');
    console.log(`  ${ok} ${checkMode ? 'verified' : 'generated'}, ${skipped} skipped, ${failed.length} failed`);
  }

  if (failed.length === 0) {
    const bundleText =
      '/** Generated by DesignDNA — offline Playground data bundle. */\n' +
      `window.__DESIGNDNA_PLAYGROUND_DATA__ = ${JSON.stringify(bundleData)};\n`;
    if (checkMode) {
      const actual = await readFile(playgroundBundlePath, 'utf8');
      if (actual !== bundleText) {
        process.exitCode = 1;
        if (!quietMode) {
          console.log('  ✗ playground/catalog-data.js out of sync');
        }
      } else if (!quietMode) {
        console.log('  ✓ playground/catalog-data.js in sync');
      }
    } else {
      await writeFile(playgroundBundlePath, bundleText);
      if (!quietMode) {
        console.log('  ✓ playground/catalog-data.js updated');
      }
    }
  }

  if (failed.length > 0) {
    process.exitCode = 1;
  }
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
